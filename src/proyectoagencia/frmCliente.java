    public void listar(){
        dtmCliente.setNumRows(0);
        for(int i=0;i<clienteController.size();i++){
            Object[] vector=new Object[9];
            vector[0] = clienteController.get(i).getCodigo();
            vector[1] = clienteController.get(i).getNombre();
            vector[2] = clienteController.get(i).getApellidoPaterno();
            vector[3] = clienteController.get(i).getApellidoMaterno();
            vector[4] = clienteController.get(i).getFechaN();
            vector[5] = clienteController.get(i).getEstado();
            vector[6] = clienteController.get(i).getDni();
            vector[7] = clienteController.get(i).getCorreo();
            vector[8] = clienteController.get(i).getSexo();
            dtmCliente.addRow(vector);
        }
        tbCliente.setModel(dtmCliente);
    }


