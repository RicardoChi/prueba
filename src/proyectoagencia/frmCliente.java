    private void btnEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarActionPerformed
    int msg = JOptionPane.showConfirmDialog(this, "Deseas Modificar el Registro","Modificar",JOptionPane.YES_NO_OPTION);
        if(msg == JOptionPane.YES_OPTION){
            Cliente cliente = clienteController.buscar(Integer.parseInt(txtCodigo.getText()));
            if(cliente != null){
                cliente.setCodigo(Integer.parseInt(txtCodigo.getText()));
                cliente.setNombre(txtNombres.getText());
                cliente.setApellidoPaterno(txtApePaterno.getText());
                cliente.setApellidoMaterno(txtApeMaterno.getText());
                cliente.setFechaN(dia.format(jdFecha.getDateFormatString()));
                cliente.setDni(txtDni.getText());
                cliente.setCorreo(txtCorreo.getText());
                cliente.setEstado(cboEstado.getSelectedItem().toString());
                cliente.setSexo(cboSexo.getSelectedItem().toString());
                listar();
                
                clienteController.grabar();
                
                JOptionPane.showMessageDialog(this,"Registro Actualizado satisfactoriamente!!!");
                
            }else{
                JOptionPane.showMessageDialog(this,"Objeto no encontrado!!!");
            }
        }
    }//GEN-LAST:event_btnEditarActionPerformed

