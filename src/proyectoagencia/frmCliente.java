    private void btnRegistarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistarActionPerformed
    int msg = JOptionPane.showConfirmDialog(this, "Deseas Grabar el Registro","Grabar",JOptionPane.YES_NO_OPTION);
    if(msg == JOptionPane.YES_OPTION){
              
        Cliente cliente = new Cliente();
        cliente.setCodigo(Integer.parseInt(txtCodigo.getText()));
        cliente.setNombre(txtNombres.getText());
        cliente.setApellidoPaterno(txtApePaterno.getText());
        cliente.setApellidoMaterno(txtApeMaterno.getText());
        cliente.setFechaN(dia.format(jdFecha.getDate()));
        cliente.setEstado(cboEstado.getSelectedItem().toString());
        cliente.setDni(txtDni.getText());
        cliente.setCorreo(txtCorreo.getText());
        cliente.setSexo(cboSexo.getSelectedItem().toString());
        
        
        
        clienteController.agregar(cliente);

        clienteController.grabar();
        listar();
        JOptionPane.showMessageDialog(this,"Registro grabado satisfactoriamente!!!");
        
    }else{
         txtNombres.requestFocus();
    }
    }//GEN-LAST:event_btnRegistarActionPerformed

